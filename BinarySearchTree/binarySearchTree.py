# A Binary Search Tree (BST) is a special type of data structure used in computer science to store and organize data efficiently.

# What is a Binary Search Tree?

# A BST is a binary tree where each node has at most two children, and it follows a specific rule:

# All values in the left subtree are less than the node’s value
# All values in the right subtree are greater than the node’s value
